response.sort(function(a, b) {
    console.log(a.name);
    if (a.name > b.name) {
        return 1;
    } else if (a.name < b.name) {
        return -1;
    }
    return 0;
});

response.forEach(function(val) {
    $main = $('<p class="button-orange">');
    $sub = $('<p class="button-orange">');
    $main.attr('data-unocode', val.unocode);
    $main.attr('data-name', val.name);
    $main.text(val.name);

    $sub.attr('data-unocode', val.unocode);
    $sub.text(val.name);
    $sub.addClass('hidden');


    $main.click(function() {
        $(this).toggleClass('hidden');
        $(`#chosencontainer > p[data-unocode=${val.unocode}]`).toggleClass('hidden');
    });

    $sub.click(function() {
        $(this).toggleClass('hidden');
        $(`#buttoncontainer > p[data-unocode=${val.unocode}]`).toggleClass('hidden');
    });

    $('#buttoncontainer').append($main);
    $('#chosencontainer').append($sub);
});


let tags = $('p[data-name');
$('#search').bind('input', function() {
    let val = $.trim(this.value.toLowerCase()); // Det här det man skriver in när man söker.
    tags.addClass('hidden');

    tags.filter(function() {
        return $(this).data('name').match(val) !== null;
    }).removeClass('hidden');
});

$('#deletebutton').click(function() {
    $('#search').val("");
    tags.removeClass('hidden');
});
